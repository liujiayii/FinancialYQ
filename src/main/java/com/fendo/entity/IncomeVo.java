package com.fendo.entity;

import java.util.Date;
/**
 * 列表显示收入的vo实体类
 * @author han
 *
 * @createDate 2018年8月6日-上午9:57:05
 */
public class IncomeVo {
	/*主键id*/
	private long id;
		
	/*收入项目标题*/
	private String title;
	/*创建时间*/
	private Date creat_time;
	/*状态     0 未生效   1 已生效*/
	private int state;
	/* （分公司）地区 */
	private String area;
	/* 姓名 */
	private String name;
	/*收入项目名称*/
	private String income_name;
	/*收入项目金额*/
	private Double money;
	/*摘要*/
	private String digest;
	public String getDigest() {
		return digest;
	}
	public void setDigest(String digest) {
		this.digest = digest;
	}
	/*总条数*/
	private int sumnumber;
	/*合计*/
	private Double sum;
	
	private long income_cost_id;
	
	private long staff_id ;
	
	public long getIncome_cost_id() {
		return income_cost_id;
	}
	public void setIncome_cost_id(long income_cost_id) {
		this.income_cost_id = income_cost_id;
	}
	public long getStaff_id() {
		return staff_id;
	}
	public void setStaff_id(long staff_id) {
		this.staff_id = staff_id;
	}
	public String getIncome_name() {
		return income_name;
	}
	public void setIncome_name(String income_name) {
		this.income_name = income_name;
	}
	public Double getSum() {
		return sum;
	}
	public void setSum(Double sum) {
		this.sum = sum;
	}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public Date getCreat_time() {
		return creat_time;
	}
	public void setCreat_time(Date creat_time) {
		this.creat_time = creat_time;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public int getSumnumber() {
		return sumnumber;
	}
	public void setSumnumber(int sumnumber) {
		this.sumnumber = sumnumber;
	}
	@Override
	public String toString() {
		return "IncomeVo [id=" + id + ", title=" + title + ", creat_time=" + creat_time + ", state=" + state + ", area="
				+ area + ", name=" + name + ", income_name=" + income_name + ", money=" + money + ", digest=" + digest
				+ ", sumnumber=" + sumnumber + ", sum=" + sum + ", income_cost_id=" + income_cost_id + ", staff_id="
				+ staff_id + "]";
	}
	
	
}

