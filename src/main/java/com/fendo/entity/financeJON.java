public class financeJON {
	private  String entityId;
	private String id;
	private String invest_amount;
	private String persistent;
	private int profit; 
	private String profit_time;
	private String sale_id;
	private String type;
	private String year_convert;
	public String getEntityId() {
		return entityId;
	}
	public void setEntityId(String entityId) {
		this.entityId = entityId;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getInvest_amount() {
		return invest_amount;
	}
	public void setInvest_amount(String invest_amount) {
		this.invest_amount = invest_amount;
	}
	public String getPersistent() {
		return persistent;
	}
	public void setPersistent(String persistent) {
		this.persistent = persistent;
	}
	
	public int getProfit() {
		return profit;
	}
	public void setProfit(int profit) {
		this.profit = profit;
	}
	public String getProfit_time() {
		return profit_time;
	}
	public void setProfit_time(String profit_time) {
		this.profit_time = profit_time;
	}
	public String getSale_id() {
		return sale_id;
	}
	public void setSale_id(String sale_id) {
		this.sale_id = sale_id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getYear_convert() {
		return year_convert;
	}
	public void setYear_convert(String year_convert) {
		this.year_convert = year_convert;
	}
	@Override
	public String toString() {
		return "financeJON [entityId=" + entityId + ", id=" + id + ", invest_amount=" + invest_amount + ", persistent="
				+ persistent + ", profit=" + profit + ", profit_time=" + profit_time + ", sale_id=" + sale_id
				+ ", type=" + type + ", year_convert=" + year_convert + "]";
	}

	

}
